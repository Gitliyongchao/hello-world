public class Student {
    public String name;
    public int firstAge;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getFirstAge() {
        return firstAge;
    }

    public void setFirstAge(int firstAge) {
        this.firstAge = firstAge;
    }
}
